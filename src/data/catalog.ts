export function getCategoryById(id: FurnitureCategory): FurnitureCategoryInfo | undefined {
  return furnitureCategories.find(c => c.id === id);
}

export function getFrontsForCategory(category: FurnitureCategory): Front[] {
  return fronts.filter(f => f.applicableCategories.includes(category));
}

export function getBasesForCategory(category: FurnitureCategory): Base[] {
  return bases.filter(b => b.applicableCategories.includes(category));
}
